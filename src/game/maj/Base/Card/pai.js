XYMJ.Pai = cc.Class.extend({
    ctor: function (pai) {  // F1 B8 等字符串
        this.object = {};
        if (typeof(pai) == 'string') {
            this.type = pai[0];  // 牌类型  J F W B T
            this.value = parseInt(pai[1]);    // 牌面值
            this.key = pai;
            this.object["type"] = this.type;
            this.object["value"] = this.value;

        } else {
            this.type = pai.type;
            this.value = pai.value;
            this.key = this.type+""+this.value;
            this.object = pai;
        }

        this.num = XYMJ.PaiFace[this.key];
    },
    objectOfPai: function () {
      return this.object;
    },

    keyOfPai: function () {
      return this.key;
    },

    numOfPai: function () {
      return this.num;
    },
    isHuaPai: function () {
        return  this.key.indexOf("H") >= 0;
    },

    isQuanZhouHuaPai:function()
    {
        return  this.key.indexOf("H") >= 0;
    },

    isQiDongHuaPai:function()
    {
        return  this.key.indexOf("H") >= 0 || this.key.indexOf("J") >= 0;
    },

    isBaiDaHuaPai:function()
    {
        return  this.key.indexOf("H") >= 0;
    },

    frameImgStandOfPai:function(){
        return XYMJ.PaiFace.ImgOfStand(this.num);
    },

    frameImgDownOfPai:function(){
        return XYMJ.PaiFace.ImgOfStand(this.num);
    },

    frameImgDownDesk:function(){
        return XYMJ.PaiFace.ImgOfDown(this.num);
    },

    frameImgRightShowOfPai:function(){
        return XYMJ.PaiFace.ImgOfRight(this.num);
    },

    frameImgLeftShowOfPai:function(){
        return XYMJ.PaiFace.ImgOfLeft(this.num);
    },

    frameImgUpShowOfPai:function(){
        return XYMJ.PaiFace.ImgOfUp(this.num);
    },

    frameImgUpBackOfPai:function(idx){
        return XYMJ.PaiFace.BackOfUp(idx);
    },

    frameImgRightBackOfPai:function(){
        return XYMJ.PaiFace.BackOfStandLeft();
    },

    frameImgLeftBackOfPai:function(){
        return XYMJ.PaiFace.BackOfStandLeft();
    },

    frameImgDownBackOfPai:function(){
        return XYMJ.PaiFace.BackOfDown();
    },
});

